import {
  existsSync,
  mkdirSync,
  mkdtempSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { afterEach, beforeEach, describe, expect, it } from "vitest";
import { installCommands, parseCommandHost } from "../src/install-commands.js";

const CODEX_SKILL_FRONTMATTER_RE =
  /^---\ndescription: .+\n---\n\n<!-- Generated by @oisincoveney\/pipeline\. -->/;
const COMMAND_FRONTMATTER_RE =
  /^---\ndescription: .+\n(?:.*\n)*?---\n\n<!-- Generated by @oisincoveney\/pipeline\. -->/;

describe("installCommands", () => {
  let dir: string;

  beforeEach(() => {
    dir = mkdtempSync(join(tmpdir(), "pipeline-commands-"));
  });

  afterEach(() => {
    rmSync(dir, { recursive: true, force: true });
  });

  it("installs all host command adapters", async () => {
    const result = await installCommands({ cwd: dir, host: "all" });

    expect(result.items.map((item) => [item.host, item.action])).toEqual([
      ["claude", "create"],
      ["opencode", "create"],
      ["codex", "create"],
      ["pi", "create"],
    ]);
    expect(existsSync(join(dir, ".claude/commands/work-next.md"))).toBe(true);
    expect(existsSync(join(dir, ".opencode/commands/work-next.md"))).toBe(true);
    expect(existsSync(join(dir, ".agents/skills/work-next/SKILL.md"))).toBe(
      true
    );
    expect(existsSync(join(dir, ".pi/prompts/work-next.md"))).toBe(true);
    expect(
      readFileSync(join(dir, ".claude/commands/work-next.md"), "utf8")
    ).toContain("Generated by @oisincoveney/pipeline");
    expect(
      readFileSync(join(dir, ".agents/skills/work-next/SKILL.md"), "utf8")
    ).toMatch(CODEX_SKILL_FRONTMATTER_RE);
    expect(
      readFileSync(join(dir, ".claude/commands/work-next.md"), "utf8")
    ).toMatch(COMMAND_FRONTMATTER_RE);
    expect(
      readFileSync(join(dir, ".opencode/commands/work-next.md"), "utf8")
    ).toMatch(COMMAND_FRONTMATTER_RE);
    expect(readFileSync(join(dir, ".pi/prompts/work-next.md"), "utf8")).toMatch(
      COMMAND_FRONTMATTER_RE
    );
  });

  it("is idempotent and check passes after install", async () => {
    await installCommands({ cwd: dir, host: "all" });

    const second = await installCommands({ cwd: dir, host: "all" });
    const checked = await installCommands({
      check: true,
      cwd: dir,
      host: "all",
    });

    expect(second.items.every((item) => item.action === "unchanged")).toBe(
      true
    );
    expect(checked.items.every((item) => item.action === "unchanged")).toBe(
      true
    );
  });

  it("dry-run reports creates without writing files", async () => {
    const result = await installCommands({
      cwd: dir,
      dryRun: true,
      host: "claude",
    });

    expect(result.items).toMatchObject([{ action: "create", host: "claude" }]);
    expect(existsSync(join(dir, ".claude/commands/work-next.md"))).toBe(false);
  });

  it("dry-run reports conflicts without throwing or overwriting", async () => {
    const path = join(dir, ".claude/commands/work-next.md");
    mkdirSync(join(dir, ".claude/commands"), { recursive: true });
    writeFileSync(path, "manual command");

    const result = await installCommands({
      cwd: dir,
      dryRun: true,
      host: "claude",
    });

    expect(result.items).toMatchObject([
      { action: "conflict", host: "claude" },
    ]);
    expect(readFileSync(path, "utf8")).toBe("manual command");
  });

  it("check fails when commands are missing", async () => {
    await expect(
      installCommands({ check: true, cwd: dir, host: "claude" })
    ).rejects.toThrow("not up to date");
  });

  it("refuses to overwrite manually edited files unless forced", async () => {
    const path = join(dir, ".claude/commands/work-next.md");
    mkdirSync(join(dir, ".claude/commands"), { recursive: true });
    writeFileSync(path, "manual command");

    await expect(installCommands({ cwd: dir, host: "claude" })).rejects.toThrow(
      "Refusing to overwrite"
    );

    const forced = await installCommands({
      cwd: dir,
      force: true,
      host: "claude",
    });

    expect(forced.items).toMatchObject([{ action: "update", host: "claude" }]);
    expect(readFileSync(path, "utf8")).toContain(
      "Generated by @oisincoveney/pipeline"
    );
  });

  it("validates host names through Commander parser hook", () => {
    expect(parseCommandHost("all")).toBe("all");
    expect(parseCommandHost("pi")).toBe("pi");
    expect(() => parseCommandHost("bad")).toThrow('Unsupported host "bad"');
  });
});
