import {
  existsSync,
  mkdirSync,
  mkdtempSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { afterEach, beforeEach, describe, expect, it } from "vitest";
import { installCommands, parseCommandHost } from "../src/install-commands.js";
import { initPipelineProject } from "../src/pipeline-init.js";

const CODEX_SKILL_FRONTMATTER_RE =
  /^---\n[\s\S]*?name: pipe\n---\n<!-- Generated by @oisincoveney\/pipeline\. -->/;
const COMMAND_FRONTMATTER_RE =
  /^---\n[\s\S]*?description: [\s\S]*?\n---\n<!-- Generated by @oisincoveney\/pipeline\. -->/;

describe("installCommands", () => {
  let dir: string;

  beforeEach(async () => {
    dir = mkdtempSync(join(tmpdir(), "pipeline-commands-"));
    await initPipelineProject({ cwd: dir });
  });

  afterEach(() => {
    rmSync(dir, { recursive: true, force: true });
  });

  it("installs all host command adapters", async () => {
    const result = await installCommands({ cwd: dir, host: "all" });

    expect(result.items.every((item) => item.action === "create")).toBe(true);
    expect(result.items.map((item) => item.path)).toEqual([
      ".claude/commands/pipe.md",
      ".claude/agents/pipeline-code-writer.md",
      ".claude/agents/pipeline-learner.md",
      ".claude/agents/pipeline-researcher.md",
      ".claude/agents/pipeline-test-writer.md",
      ".claude/agents/pipeline-verifier.md",
      ".opencode/commands/pipe.md",
      ".opencode/agents/pipeline-orchestrator.md",
      ".opencode/agents/pipeline-code-writer.md",
      ".opencode/agents/pipeline-learner.md",
      ".opencode/agents/pipeline-researcher.md",
      ".opencode/agents/pipeline-test-writer.md",
      ".opencode/agents/pipeline-verifier.md",
      ".agents/skills/pipe/SKILL.md",
      ".codex/agents/pipeline-code-writer.toml",
      ".codex/agents/pipeline-learner.toml",
      ".codex/agents/pipeline-researcher.toml",
      ".codex/agents/pipeline-test-writer.toml",
      ".codex/agents/pipeline-verifier.toml",
      ".kimi/commands/pipe.md",
      ".kimi/agents/pipeline-code-writer.md",
      ".kimi/agents/pipeline-learner.md",
      ".kimi/agents/pipeline-researcher.md",
      ".kimi/agents/pipeline-test-writer.md",
      ".kimi/agents/pipeline-verifier.md",
      ".pi/prompts/pipe.md",
      ".pi/extensions/pipe.ts",
    ]);
    for (const item of result.items) {
      expect(existsSync(join(dir, item.path))).toBe(true);
    }
    expect(
      readFileSync(join(dir, ".claude/commands/pipe.md"), "utf8")
    ).toContain("Generated by @oisincoveney/pipeline");
    expect(
      readFileSync(join(dir, ".agents/skills/pipe/SKILL.md"), "utf8")
    ).toMatch(CODEX_SKILL_FRONTMATTER_RE);
    expect(readFileSync(join(dir, ".claude/commands/pipe.md"), "utf8")).toMatch(
      COMMAND_FRONTMATTER_RE
    );
    expect(
      readFileSync(join(dir, ".opencode/commands/pipe.md"), "utf8")
    ).toMatch(COMMAND_FRONTMATTER_RE);
    expect(readFileSync(join(dir, ".pi/prompts/pipe.md"), "utf8")).toMatch(
      COMMAND_FRONTMATTER_RE
    );
    for (const item of result.items) {
      const content = readFileSync(join(dir, item.path), "utf8");
      if (item.path.endsWith(".ts")) {
        expect(content).toContain(
          `// @oisincoveney/pipeline:host=${item.host}`
        );
      } else if (!item.path.endsWith(".toml")) {
        expect(content).toContain(
          `<!-- @oisincoveney/pipeline:host=${item.host} -->`
        );
      }
      expect(content).not.toContain("version=");
    }
  });

  it("generates host resources that configure native agents instead of shelling to the CLI", async () => {
    await installCommands({ cwd: dir, host: "all" });

    const claudeCommand = readFileSync(
      join(dir, ".claude/commands/pipe.md"),
      "utf8"
    );
    const opencodeCommand = readFileSync(
      join(dir, ".opencode/commands/pipe.md"),
      "utf8"
    );
    const codexSkill = readFileSync(
      join(dir, ".agents/skills/pipe/SKILL.md"),
      "utf8"
    );
    const piExtension = readFileSync(
      join(dir, ".pi/extensions/pipe.ts"),
      "utf8"
    );

    expect(claudeCommand).toContain("pipeline-researcher");
    expect(opencodeCommand).toContain("agent: pipeline-orchestrator");
    expect(opencodeCommand).toContain("subtask: true");
    expect(codexSkill).toContain("$pipe <task description>");
    expect(piExtension).toContain("export default function pipelineWorkNext");
    expect(piExtension).toContain('pi.registerCommand("pipe", {');
    expect(piExtension).toContain("WORKFLOW_NODES");
    expect(piExtension).toContain("pipeline-researcher");
    expect(piExtension).toContain('"run-chain"');
    expect(piExtension).not.toContain("sendUserMessage(renderWorkflowPrompt");

    for (const content of [
      claudeCommand,
      opencodeCommand,
      codexSkill,
      piExtension,
    ]) {
      expect(content).not.toContain("bunx @oisincoveney/pipeline");
      expect(content).not.toContain("./node_modules/.bin/pipe");
    }
  });

  it("generates discoverable agent configuration for each agent host", async () => {
    await installCommands({ cwd: dir, host: "all" });

    expect(
      readFileSync(join(dir, ".claude/agents/pipeline-researcher.md"), "utf8")
    ).toContain("name: pipeline-researcher");
    expect(
      readFileSync(
        join(dir, ".opencode/agents/pipeline-orchestrator.md"),
        "utf8"
      )
    ).toContain("mode: primary");
    expect(
      readFileSync(join(dir, ".opencode/agents/pipeline-researcher.md"), "utf8")
    ).toContain("mode: subagent");
    expect(
      readFileSync(join(dir, ".codex/agents/pipeline-researcher.toml"), "utf8")
    ).toContain('name = "pipeline-researcher"');
    expect(
      readFileSync(join(dir, ".kimi/agents/pipeline-researcher.md"), "utf8")
    ).toContain("name: pipeline-researcher");
  });

  it("projects configured orchestrator grants into every host surface", async () => {
    mkdirSync(join(dir, ".agents/skills/orchestrator"), { recursive: true });
    writeFileSync(
      join(dir, ".agents/skills/orchestrator/SKILL.md"),
      "# Orchestrator skill\n"
    );
    const configPath = join(dir, ".pipeline/profiles.yaml");
    const config = readFileSync(configPath, "utf8")
      .replace(
        "skills: {}",
        "skills:\n  orchestrator:\n    path: .agents/skills/orchestrator/SKILL.md"
      )
      .replace(
        "mcp_servers: {}",
        "mcp_servers:\n  knowledge-base:\n    command: node\n    args: [kb.js]"
      )
      .replace(
        "  orchestrator:\n    runner: codex",
        "  orchestrator:\n    runner: codex\n    model: gpt-5-orchestrator"
      )
      .replace(
        "    tools: [read, list, grep, glob, bash]",
        "    skills: [orchestrator]\n    mcp_servers: [knowledge-base]\n    tools: [read, list, grep, glob, bash]"
      );
    writeFileSync(configPath, config);

    await installCommands({ cwd: dir, force: true, host: "all" });

    const surfaces = [
      ".claude/commands/pipe.md",
      ".opencode/commands/pipe.md",
      ".opencode/agents/pipeline-orchestrator.md",
      ".agents/skills/pipe/SKILL.md",
      ".kimi/commands/pipe.md",
      ".pi/prompts/pipe.md",
      ".pi/extensions/pipe.ts",
    ].map((path) => readFileSync(join(dir, path), "utf8"));

    for (const content of surfaces) {
      expect(content).toContain("Configured orchestrator:");
      expect(content).toContain("model: gpt-5-orchestrator");
      expect(content).toContain("skills: orchestrator");
      expect(content).toContain("mcp_servers: knowledge-base");
    }
  });

  it("is idempotent and check passes after install", async () => {
    await installCommands({ cwd: dir, host: "all" });

    const second = await installCommands({ cwd: dir, host: "all" });
    const checked = await installCommands({
      check: true,
      cwd: dir,
      host: "all",
    });

    expect(second.items.every((item) => item.action === "unchanged")).toBe(
      true
    );
    expect(checked.items.every((item) => item.action === "unchanged")).toBe(
      true
    );
  });

  it("dry-run reports creates without writing files", async () => {
    const result = await installCommands({
      cwd: dir,
      dryRun: true,
      host: "claude",
    });

    expect(result.items.every((item) => item.action === "create")).toBe(true);
    expect(result.items.every((item) => item.host === "claude")).toBe(true);
    expect(existsSync(join(dir, ".claude/commands/pipe.md"))).toBe(false);
  });

  it("dry-run reports conflicts without throwing or overwriting", async () => {
    const path = join(dir, ".claude/commands/pipe.md");
    mkdirSync(join(dir, ".claude/commands"), { recursive: true });
    writeFileSync(path, "manual command");

    const result = await installCommands({
      cwd: dir,
      dryRun: true,
      host: "claude",
    });

    expect(result.items[0]).toMatchObject({
      action: "conflict",
      host: "claude",
      path: ".claude/commands/pipe.md",
    });
    expect(readFileSync(path, "utf8")).toBe("manual command");
  });

  it("check fails when commands are missing", async () => {
    await expect(
      installCommands({ check: true, cwd: dir, host: "claude" })
    ).rejects.toThrow("not up to date");
  });

  it("refuses to overwrite manually edited files unless forced", async () => {
    const path = join(dir, ".claude/commands/pipe.md");
    mkdirSync(join(dir, ".claude/commands"), { recursive: true });
    writeFileSync(path, "manual command");

    await expect(installCommands({ cwd: dir, host: "claude" })).rejects.toThrow(
      "Refusing to overwrite"
    );

    const forced = await installCommands({
      cwd: dir,
      force: true,
      host: "claude",
    });

    expect(forced.items[0]).toMatchObject({
      action: "update",
      host: "claude",
      path: ".claude/commands/pipe.md",
    });
    expect(readFileSync(path, "utf8")).toContain(
      "Generated by @oisincoveney/pipeline"
    );
  });

  it("validates host names through Commander parser hook", () => {
    expect(parseCommandHost("all")).toBe("all");
    expect(parseCommandHost("kimi")).toBe("kimi");
    expect(parseCommandHost("pi")).toBe("pi");
    expect(() => parseCommandHost("bad")).toThrow('Unsupported host "bad"');
  });
});
