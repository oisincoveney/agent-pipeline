function updatePipelineYaml(
  dir: string,
  mutate: (config: MutablePipelineYaml) => void
): void {
  const configPath = join(dir, ".pipeline/pipeline.yaml");
  const config = parse(readFileSync(configPath, "utf8")) as MutablePipelineYaml;
  mutate(config);
  writeFileSync(configPath, stringify(config));
}

function updateProfilesYaml(
  dir: string,
  mutate: (config: MutableProfilesYaml) => void
): void {
  const configPath = join(dir, ".pipeline/profiles.yaml");
  const config = parse(readFileSync(configPath, "utf8")) as MutableProfilesYaml;
  mutate(config);
  writeFileSync(configPath, stringify(config));
}

function setProfileRunner(
  dir: string,
  profileId: string,
  runner: string,
  options: { dropMcpServers?: boolean; dropSkills?: boolean } = {}
): void {
  updateProfilesYaml(dir, (config) => {
    const profiles = config.profiles;
    const profile = profiles?.[profileId];
    if (!profile) {
      throw new Error(`Missing test profile ${profileId}`);
    }
    const droppedKeys = new Set([
      ...(options.dropSkills ? ["skills"] : []),
      ...(options.dropMcpServers ? ["mcp_servers"] : []),
    ]);
    profiles[profileId] = Object.fromEntries(
      Object.entries({ ...profile, runner }).filter(
        ([key]) => !droppedKeys.has(key)
      )
    );
  });
}

