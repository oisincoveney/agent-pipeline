import { existsSync, readFileSync } from "node:fs";
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import matter from "gray-matter";
import { stringify as stringifyToml } from "smol-toml";
import {
  PIPELINE_AGENTS,
  type PipelineSpecAgent,
  renderPipelineWorkflow,
} from "./pipeline-spec.js";

const GENERATED_MARKER = "<!-- Generated by @oisincoveney/pipeline. -->";
const GENERATED_TS_MARKER = "// Generated by @oisincoveney/pipeline.";
const OWNER_MARKER_PREFIX = "<!-- @oisincoveney/pipeline:";
const OWNER_TS_MARKER_PREFIX = "// @oisincoveney/pipeline:";

export const COMMAND_HOSTS = ["claude", "opencode", "codex", "pi"] as const;

export type CommandHost = (typeof COMMAND_HOSTS)[number];
export type CommandHostSelection = CommandHost | "all";
export type InstallAction = "conflict" | "create" | "unchanged" | "update";

export interface CommandInstallPlanItem {
  action: InstallAction;
  host: CommandHost;
  invocation: string;
  path: string;
}

export interface InstallCommandsOptions {
  check?: boolean;
  cwd?: string;
  dryRun?: boolean;
  force?: boolean;
  host?: CommandHostSelection;
}

export interface InstallCommandsResult {
  items: CommandInstallPlanItem[];
}

interface CommandDefinition {
  content: string;
  host: CommandHost;
  invocation: string;
  path: string;
}

function header(host: CommandHost): string {
  return [GENERATED_MARKER, `${OWNER_MARKER_PREFIX}host=${host} -->`, ""].join(
    "\n"
  );
}

function tsHeader(host: CommandHost): string {
  return [
    GENERATED_TS_MARKER,
    `${OWNER_TS_MARKER_PREFIX}host=${host}`,
    "",
  ].join("\n");
}

function markdown(data: Record<string, unknown>, body: string): string {
  return `${matter.stringify(body.trimEnd(), data).trimEnd()}\n`;
}

function agentNames(): string {
  return PIPELINE_AGENTS.map((agent) => `\`${agent.name}\``).join(", ");
}

function claudeTools(agent: PipelineSpecAgent): string {
  const toolNames = new Set<string>();
  for (const tool of agent.tools) {
    switch (tool) {
      case "bash":
        toolNames.add("Bash");
        break;
      case "edit":
        toolNames.add("Edit");
        break;
      case "glob":
        toolNames.add("Glob");
        break;
      case "grep":
        toolNames.add("Grep");
        break;
      case "list":
        toolNames.add("LS");
        break;
      case "read":
        toolNames.add("Read");
        break;
      case "write":
        toolNames.add("Write");
        break;
      default:
        break;
    }
  }
  return [...toolNames].join(", ");
}

function claudeAgentDefinition(agent: PipelineSpecAgent): CommandDefinition {
  return {
    content: markdown(
      {
        description: agent.description,
        name: agent.name,
        tools: claudeTools(agent),
      },
      [
        header("claude").trimEnd(),
        "",
        agent.description,
        "",
        "Follow the oisin pipeline phase contract. Keep changes scoped to your assigned role and return concrete evidence to the orchestrating `/pipe` command.",
        "",
      ].join("\n")
    ),
    host: "claude",
    invocation: "/pipe <ticket id or task description>",
    path: `.claude/agents/${agent.name}.md`,
  };
}

