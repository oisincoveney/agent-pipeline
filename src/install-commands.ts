import { existsSync, readFileSync } from "node:fs";
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";

const PACKAGE_NAME = "@oisincoveney/pipeline";
const GENERATED_MARKER = "<!-- Generated by @oisincoveney/pipeline. -->";
const UPDATE_MARKER_PREFIX = "<!-- @oisincoveney/pipeline:";

export const COMMAND_HOSTS = ["claude", "opencode", "codex", "pi"] as const;

export type CommandHost = (typeof COMMAND_HOSTS)[number];
export type CommandHostSelection = CommandHost | "all";
export type InstallAction = "conflict" | "create" | "unchanged" | "update";

export interface CommandInstallPlanItem {
  action: InstallAction;
  host: CommandHost;
  invocation: string;
  path: string;
}

export interface InstallCommandsOptions {
  check?: boolean;
  cwd?: string;
  dryRun?: boolean;
  force?: boolean;
  host?: CommandHostSelection;
}

export interface InstallCommandsResult {
  items: CommandInstallPlanItem[];
}

interface CommandDefinition {
  content: string;
  host: CommandHost;
  invocation: string;
  path: string;
}

function packageVersion(): string {
  try {
    const pkg = JSON.parse(
      readFileSync(new URL("../package.json", import.meta.url), "utf8")
    ) as { version?: string };
    return pkg.version ?? "unknown";
  } catch {
    return "unknown";
  }
}

function header(host: CommandHost): string {
  return [
    GENERATED_MARKER,
    `${UPDATE_MARKER_PREFIX}host=${host}; version=${packageVersion()} -->`,
    "",
  ].join("\n");
}

function pipelineCommand(args: string): string {
  return [
    "if [ -x ./node_modules/.bin/work-next ]; then",
    `  ./node_modules/.bin/work-next "${args}";`,
    "else",
    `  bunx ${PACKAGE_NAME} work-next "${args}";`,
    "fi",
  ].join(" ");
}

function sharedInstructions(args: string): string {
  return [
    "Run the oisin pipeline mechanically for the provided task.",
    "",
    "Do not reimplement the pipeline inside this chat. Execute the installed CLI command and report its result.",
    "",
    "Task:",
    "",
    "```",
    args,
    "```",
  ].join("\n");
}

function claudeDefinition(): CommandDefinition {
  const args = "$ARGUMENTS";
  return {
    content: [
      header("claude"),
      "---",
      "description: Run the oisin pipeline for a task",
      'argument-hint: "<ticket id or task description>"',
      "allowed-tools: Bash(./node_modules/.bin/work-next:*), Bash(bunx @oisincoveney/pipeline:*)",
      "---",
      "",
      sharedInstructions(args),
      "",
      `!\`${pipelineCommand(args)}\``,
      "",
    ].join("\n"),
    host: "claude",
    invocation: "/work-next <ticket id or task description>",
    path: ".claude/commands/work-next.md",
  };
}

function opencodeDefinition(): CommandDefinition {
  const args = "$ARGUMENTS";
  return {
    content: [
      header("opencode"),
      "---",
      "description: Run the oisin pipeline for a task",
      "agent: build",
      "---",
      "",
      sharedInstructions(args),
      "",
      `!\`${pipelineCommand(args)}\``,
      "",
    ].join("\n"),
    host: "opencode",
    invocation: "/work-next <ticket id or task description>",
    path: ".opencode/commands/work-next.md",
  };
}

function piDefinition(): CommandDefinition {
  const args = "$ARGUMENTS";
  return {
    content: `${header("pi")}---\ndescription: Run the oisin pipeline for a task\nargument-hint: "<ticket id or task description>"\n---\n\n${sharedInstructions(args)}\n\n!${pipelineCommand(args)}\n`,
    host: "pi",
    invocation: "/work-next <ticket id or task description>",
    path: ".pi/prompts/work-next.md",
  };
}

function codexDefinition(): CommandDefinition {
  return {
    content: `${header("codex")}---\ndescription: Run the installed oisin pipeline CLI for a task. Use when the user asks to run work-next or the oisin pipeline.\n---\n\nCodex currently does not support project-defined custom slash commands. Use this skill with:\n\n\`\`\`\n/use work-next <ticket id or task description>\n\`\`\`\n\nWhen invoked, run this command from the repository root, replacing the argument text with the requested task:\n\n\`\`\`sh\n${pipelineCommand("<ticket id or task description>")}\n\`\`\`\n\nReport the pipeline outcome and any failure evidence.\n`,
    host: "codex",
    invocation: "/use work-next <ticket id or task description>",
    path: ".agents/skills/work-next/SKILL.md",
  };
}

function definitionsFor(host: CommandHostSelection): CommandDefinition[] {
  const definitions: Record<CommandHost, () => CommandDefinition> = {
    claude: claudeDefinition,
    codex: codexDefinition,
    opencode: opencodeDefinition,
    pi: piDefinition,
  };
  const hosts = host === "all" ? COMMAND_HOSTS : [host];
  return hosts.map((name) => definitions[name]());
}

function actionFor(
  path: string,
  content: string,
  force: boolean
): InstallAction {
  if (!existsSync(path)) {
    return "create";
  }
  const current = readFileSync(path, "utf8");
  if (current === content) {
    return "unchanged";
  }
  if (!(current.includes(GENERATED_MARKER) || force)) {
    return "conflict";
  }
  return "update";
}

export async function installCommands(
  options: InstallCommandsOptions = {}
): Promise<InstallCommandsResult> {
  const cwd = options.cwd ?? process.cwd();
  const host = options.host ?? "all";
  const items: CommandInstallPlanItem[] = [];

  for (const definition of definitionsFor(host)) {
    const target = join(cwd, definition.path);
    const action = actionFor(
      target,
      definition.content,
      Boolean(options.force)
    );
    items.push({
      action,
      host: definition.host,
      invocation: definition.invocation,
      path: definition.path,
    });

    if (options.check || options.dryRun || action === "unchanged") {
      continue;
    }
    if (action === "conflict") {
      continue;
    }

    await mkdir(dirname(target), { recursive: true });
    await writeFile(target, definition.content);
  }

  if (!options.dryRun && items.some((item) => item.action === "conflict")) {
    throw new Error(
      [
        "Refusing to overwrite manually edited command files.",
        ...items
          .filter((item) => item.action === "conflict")
          .map((item) => `- ${item.path}`),
        "Re-run with --force to overwrite them.",
      ].join("\n")
    );
  }

  if (options.check && items.some((item) => item.action !== "unchanged")) {
    throw new Error(
      [
        "Installed command files are not up to date.",
        ...items
          .filter((item) => item.action !== "unchanged")
          .map((item) => `- ${item.path}: ${item.action}`),
      ].join("\n")
    );
  }

  return { items };
}

export function parseCommandHost(
  value: string | undefined
): CommandHostSelection {
  const host = value ?? "all";
  if (host === "all" || COMMAND_HOSTS.includes(host as CommandHost)) {
    return host as CommandHostSelection;
  }
  throw new Error(
    `Unsupported host "${host}". Supported values: all, ${COMMAND_HOSTS.join(", ")}.`
  );
}

export function formatInstallCommandsResult(
  result: InstallCommandsResult
): string {
  return result.items
    .map(
      (item) => `${item.action} ${item.host}: ${item.path} (${item.invocation})`
    )
    .join("\n");
}
