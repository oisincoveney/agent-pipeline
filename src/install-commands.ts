function opencodePermission(agent: PipelineSpecAgent): Record<string, string> {
  return {
    bash: agent.tools.includes("bash") ? "allow" : "deny",
    edit: agent.tools.includes("edit") ? "allow" : "deny",
    glob: "allow",
    grep: "allow",
    list: "allow",
    read: "allow",
    task: "deny",
    write: agent.tools.includes("write") ? "allow" : "deny",
  };
}

function opencodeAgentDefinition(
  agent: PipelineSpecAgent,
  mode: "primary" | "subagent"
): CommandDefinition {
  const permission =
    mode === "primary"
      ? {
          bash: "allow",
          edit: "allow",
          glob: "allow",
          grep: "allow",
          list: "allow",
          read: "allow",
          task: "allow",
          write: "allow",
        }
      : opencodePermission(agent);

  return {
    content: markdown(
      {
        description: agent.description,
        mode,
        permission,
      },
      [
        header("opencode").trimEnd(),
        "",
        agent.description,
        "",
        mode === "primary"
          ? `Orchestrate the oisin pipeline and delegate phase work to ${agentNames()} with the Task tool.`
          : "Follow the oisin pipeline phase contract for your assigned role and return concrete evidence to the orchestrator.",
        "",
      ].join("\n")
    ),
    host: "opencode",
    invocation: "/pipe <ticket id or task description>",
    path: `.opencode/agents/${agent.name}.md`,
  };
}

