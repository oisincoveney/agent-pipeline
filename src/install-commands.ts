function piDefinitions(config: PipelineConfig): CommandDefinition[] {
  return [
    {
      content: markdown(
        {
          "argument-hint": "<task description>",
          description: "Run the configured pipeline workflow with Pi subagents",
        },
        [header("pi").trimEnd(), "", workflowSummary(config)].join("\n")
      ),
      host: "pi",
      invocation: "/pipe <task description>",
      path: ".pi/prompts/pipe.md",
    },
    {
      content: [
        tsHeader("pi").trimEnd(),
        "",
        "interface PiCommand {",
        "  name: string;",
        "}",
        "interface PiCommandContext {",
        "  sendUserMessage(message: string): Promise<void> | void;",
        "  ui: { notify(message: string, type?: string): void };",
        "}",
        "interface PiExtensionApi {",
        "  getCommands?(): PiCommand[];",
        "  registerCommand(",
        "    name: string,",
        "    command: {",
        "      description: string;",
        "      handler(args: string, ctx: PiCommandContext): Promise<void> | void;",
        "    }",
        "  ): void;",
        "}",
        "",
        ...piWorkflowNodesLiteral(config),
        "",
        "function renderSubagentCommand(task: string): string {",
        '  const chain = WORKFLOW_NODES.filter((node) => node.kind === "agent")',
        "    .map((node) => `" +
          "$" +
          "{String(node.agent)}: " +
          "$" +
          "{node.id} " +
          "$" +
          "{task}" +
          "`)",
        '    .join(" -> ");',
        ["  return `/chain ", "$", "{JSON.stringify(chain)}`;"].join(""),
        "}",
        "",
        "export default function pipelineWorkNext(pi: PiExtensionApi): void {",
        '  pi.registerCommand("pipe", {',
        '    description: "Run the configured pipeline with Pi subagents",',
        "    handler: async (args: string, ctx) => {",
        '      const task = String(args ?? "").trim();',
        "      if (!task) {",
        '        ctx.ui.notify("Usage: /pipe <task description>", "error");',
        "        return;",
        "      }",
        "      const commands =",
        '        typeof pi.getCommands === "function" ? pi.getCommands() : [];',
        "      const hasPiSubagents = commands.some((command) =>",
        '        ["run", "chain", "parallel", "run-chain", "subagents-doctor"].some(',
        "          (name) => command.name === name || command.name.startsWith(`" +
          "$" +
          "{name}:" +
          "`)",
        "        )",
        "      );",
        "      if (!hasPiSubagents) {",
        '        ctx.ui.notify("Install pi-subagents before running /pipe.", "error");',
        "        return;",
        "      }",
        "      await ctx.sendUserMessage(renderSubagentCommand(task));",
        "    },",
        "  });",
        "}",
        "",
      ].join("\n"),
      host: "pi",
      invocation: "/pipe <task description>",
      path: ".pi/extensions/pipe.ts",
    },
  ];
}

function piWorkflowNodesLiteral(config: PipelineConfig): string[] {
  const plan = compileWorkflowPlan(config);
  return [
    "const WORKFLOW_NODES = [",
    ...plan.topologicalOrder.flatMap((node) => [
      "  {",
      `    agent: ${JSON.stringify(node.agent ?? null)},`,
      `    id: ${JSON.stringify(node.id)},`,
      `    kind: ${JSON.stringify(node.kind)},`,
      `    needs: ${JSON.stringify(node.needs)},`,
      "  },",
    ]),
    "] as const;",
  ];
}

function instructionsPointer(agent: PipelineConfig["agents"][string]): string {
  if (agent.instructions.path) {
    return `Instructions: ${agent.instructions.path}`;
  }
  return `Instructions:\n${agent.instructions.inline ?? ""}`;
}

function definitionsFor(
  host: CommandHostSelection,
  config: PipelineConfig
): CommandDefinition[] {
  const definitions: Record<CommandHost, () => CommandDefinition[]> = {
    claude: () => claudeDefinitions(config),
    codex: () => codexDefinitions(config),
    kimi: () => kimiDefinitions(config),
    opencode: () => opencodeDefinitions(config),
    pi: () => piDefinitions(config),
  };
  const hosts = host === "all" ? COMMAND_HOSTS : [host];
  return hosts.flatMap((name) => definitions[name]());
}

function actionFor(
  path: string,
  content: string,
  force: boolean
): InstallAction {
  if (!existsSync(path)) {
    return "create";
  }
  const current = readFileSync(path, "utf8");
  if (current === content) {
    return "unchanged";
  }
  if (
    !(
      current.includes(GENERATED_MARKER) ||
      current.includes(GENERATED_TS_MARKER) ||
      force
    )
  ) {
    return "conflict";
  }
  return "update";
}

export async function installCommands(
  options: InstallCommandsOptions = {}
): Promise<InstallCommandsResult> {
  const cwd = options.cwd ?? process.cwd();
  const host = options.host ?? "all";
  const config = loadPipelineConfig(cwd);
  const items: CommandInstallPlanItem[] = [];

  for (const definition of definitionsFor(host, config)) {
    const target = join(cwd, definition.path);
    const action = actionFor(
      target,
      definition.content,
      Boolean(options.force)
    );
    items.push({
      action,
      host: definition.host,
      invocation: definition.invocation,
      path: definition.path,
    });

    if (options.check || options.dryRun || action === "unchanged") {
      continue;
    }
    if (action === "conflict") {
      continue;
    }

    await mkdir(dirname(target), { recursive: true });
    await writeFile(target, definition.content);
  }

  if (!options.dryRun && items.some((item) => item.action === "conflict")) {
    throw new Error(
      [
        "Refusing to overwrite manually edited command files.",
        ...items
          .filter((item) => item.action === "conflict")
          .map((item) => `- ${item.path}`),
        "Re-run with --force to overwrite them.",
      ].join("\n")
    );
  }

  if (options.check && items.some((item) => item.action !== "unchanged")) {
    throw new Error(
      [
        "Installed command files are not up to date.",
        ...items
          .filter((item) => item.action !== "unchanged")
          .map((item) => `- ${item.path}: ${item.action}`),
      ].join("\n")
    );
  }

  return { items };
}

export function parseCommandHost(
  value: string | undefined
): CommandHostSelection {
  const host = value ?? "all";
  if (host === "all" || COMMAND_HOSTS.includes(host as CommandHost)) {
    return host as CommandHostSelection;
  }
  throw new Error(
    `Unsupported host "${host}". Supported values: all, ${COMMAND_HOSTS.join(", ")}.`
  );
}

export function formatInstallCommandsResult(
  result: InstallCommandsResult
): string {
  return result.items
    .map(
      (item) => `${item.action} ${item.host}: ${item.path} (${item.invocation})`
    )
    .join("\n");
}
