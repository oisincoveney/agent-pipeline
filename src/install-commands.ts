import { existsSync, readFileSync } from "node:fs";
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import matter from "gray-matter";
import { stringify as stringifyToml } from "smol-toml";
import { loadPipelineConfig, type PipelineConfig } from "./mastra/config.js";
import { compileWorkflowPlan } from "./workflow-planner.js";

const GENERATED_MARKER = "<!-- Generated by @oisincoveney/pipeline. -->";
const GENERATED_TS_MARKER = "// Generated by @oisincoveney/pipeline.";
const OWNER_MARKER_PREFIX = "<!-- @oisincoveney/pipeline:";
const OWNER_TS_MARKER_PREFIX = "// @oisincoveney/pipeline:";

export const COMMAND_HOSTS = [
  "claude",
  "opencode",
  "codex",
  "kimi",
  "pi",
] as const;

export type CommandHost = (typeof COMMAND_HOSTS)[number];
export type CommandHostSelection = CommandHost | "all";
export type InstallAction = "conflict" | "create" | "unchanged" | "update";

export interface CommandInstallPlanItem {
  action: InstallAction;
  host: CommandHost;
  invocation: string;
  path: string;
}

export interface InstallCommandsOptions {
  check?: boolean;
  cwd?: string;
  dryRun?: boolean;
  force?: boolean;
  host?: CommandHostSelection;
}

export interface InstallCommandsResult {
  items: CommandInstallPlanItem[];
}

interface CommandDefinition {
  content: string;
  host: CommandHost;
  invocation: string;
  path: string;
}

type AgentEntry = [string, PipelineConfig["agents"][string]];

function header(host: CommandHost): string {
  return [GENERATED_MARKER, `${OWNER_MARKER_PREFIX}host=${host} -->`, ""].join(
    "\n"
  );
}

function tsHeader(host: CommandHost): string {
  return [
    GENERATED_TS_MARKER,
    `${OWNER_TS_MARKER_PREFIX}host=${host}`,
    "",
  ].join("\n");
}

function markdown(data: Record<string, unknown>, body: string): string {
  return `${matter.stringify(body.trimEnd(), data).trimEnd()}\n`;
}

function agentEntries(config: PipelineConfig): AgentEntry[] {
  return Object.entries(config.agents).sort(([a], [b]) => a.localeCompare(b));
}

function agentNames(config: PipelineConfig): string {
  return agentEntries(config)
    .map(([name]) => `\`${name}\``)
    .join(", ");
}

function workflowSummary(config: PipelineConfig): string {
  const plan = compileWorkflowPlan(config);
  return [
    `Workflow: ${plan.workflowId}`,
    "",
    ...plan.topologicalOrder.map((node) => {
      const parts = [
        `- ${node.id}`,
        `kind=${node.kind}`,
        node.agent ? `agent=${node.agent}` : "",
        node.needs.length ? `needs=${node.needs.join(",")}` : "needs=none",
      ].filter(Boolean);
      return parts.join(" ");
    }),
  ].join("\n");
}

function grants(agent: PipelineConfig["agents"][string]): string {
  return [
    `tools: ${(agent.tools ?? []).join(", ") || "none"}`,
    `rules: ${(agent.rules ?? []).join(", ") || "none"}`,
    `skills: ${(agent.skills ?? []).join(", ") || "none"}`,
    `mcp_servers: ${(agent.mcp_servers ?? []).join(", ") || "none"}`,
    `filesystem: ${agent.filesystem?.mode ?? "default"}`,
    `network: ${agent.network?.mode ?? "default"}`,
    `output: ${agent.output?.format ?? "text"}`,
  ].join("\n");
}

function claudeTools(agent: PipelineConfig["agents"][string]): string {
  const mapped = new Map([
    ["bash", "Bash"],
    ["edit", "Edit"],
    ["glob", "Glob"],
    ["grep", "Grep"],
    ["list", "LS"],
    ["read", "Read"],
    ["write", "Write"],
  ]);
  return (agent.tools ?? [])
    .flatMap((tool) => {
      const value = mapped.get(tool);
      return value ? [value] : [];
    })
    .join(", ");
}

function claudeDefinitions(config: PipelineConfig): CommandDefinition[] {
  return [
    {
      content: markdown(
        {
          "argument-hint": "<task description>",
          description: "Run the configured pipeline workflow",
        },
        [
          header("claude").trimEnd(),
          "",
          workflowSummary(config),
          "",
          `Delegate work only to configured agents: ${agentNames(config)}.`,
        ].join("\n")
      ),
      host: "claude",
      invocation: "/pipe <task description>",
      path: ".claude/commands/pipe.md",
    },
    ...agentEntries(config).map(([id, agent]) => ({
      content: markdown(
        {
          description: agent.description ?? id,
          name: id,
          tools: claudeTools(agent),
        },
        [
          header("claude").trimEnd(),
          "",
          agent.description ?? id,
          "",
          "Configured grants:",
          grants(agent),
          "",
          instructionsPointer(agent),
        ].join("\n")
      ),
      host: "claude" as const,
      invocation: "/pipe <task description>",
      path: `.claude/agents/${id}.md`,
    })),
  ];
}

function opencodePermission(
  agent: PipelineConfig["agents"][string]
): Record<string, string> {
  const allowed = new Set(agent.tools ?? []);
  return {
    bash: allowed.has("bash") ? "allow" : "deny",
    edit: allowed.has("edit") ? "allow" : "deny",
    glob: allowed.has("glob") ? "allow" : "deny",
    grep: allowed.has("grep") ? "allow" : "deny",
    list: allowed.has("list") ? "allow" : "deny",
    read: allowed.has("read") ? "allow" : "deny",
    task: allowed.has("task") ? "allow" : "deny",
    write: allowed.has("write") ? "allow" : "deny",
  };
}

