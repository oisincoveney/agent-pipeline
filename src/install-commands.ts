function definitionsFor(host: CommandHostSelection): CommandDefinition[] {
  const definitions: Record<CommandHost, () => CommandDefinition[]> = {
    claude: claudeDefinitions,
    codex: codexDefinitions,
    opencode: opencodeDefinitions,
    pi: piDefinitions,
  };
  const hosts = host === "all" ? COMMAND_HOSTS : [host];
  return hosts.flatMap((name) => definitions[name]());
}

function actionFor(
  path: string,
  content: string,
  force: boolean
): InstallAction {
  if (!existsSync(path)) {
    return "create";
  }
  const current = readFileSync(path, "utf8");
  if (current === content) {
    return "unchanged";
  }
  if (
    !(
      current.includes(GENERATED_MARKER) ||
      current.includes(GENERATED_TS_MARKER) ||
      force
    )
  ) {
    return "conflict";
  }
  return "update";
}

export async function installCommands(
  options: InstallCommandsOptions = {}
): Promise<InstallCommandsResult> {
  const cwd = options.cwd ?? process.cwd();
  const host = options.host ?? "all";
  const items: CommandInstallPlanItem[] = [];

  for (const definition of definitionsFor(host)) {
    const target = join(cwd, definition.path);
    const action = actionFor(
      target,
      definition.content,
      Boolean(options.force)
    );
    items.push({
      action,
      host: definition.host,
      invocation: definition.invocation,
      path: definition.path,
    });

    if (options.check || options.dryRun || action === "unchanged") {
      continue;
    }
    if (action === "conflict") {
      continue;
    }

    await mkdir(dirname(target), { recursive: true });
    await writeFile(target, definition.content);
  }

  if (!options.dryRun && items.some((item) => item.action === "conflict")) {
    throw new Error(
      [
        "Refusing to overwrite manually edited command files.",
        ...items
          .filter((item) => item.action === "conflict")
          .map((item) => `- ${item.path}`),
        "Re-run with --force to overwrite them.",
      ].join("\n")
    );
  }

  if (options.check && items.some((item) => item.action !== "unchanged")) {
    throw new Error(
      [
        "Installed command files are not up to date.",
        ...items
          .filter((item) => item.action !== "unchanged")
          .map((item) => `- ${item.path}: ${item.action}`),
      ].join("\n")
    );
  }

  return { items };
}

export function parseCommandHost(
  value: string | undefined
): CommandHostSelection {
  const host = value ?? "all";
  if (host === "all" || COMMAND_HOSTS.includes(host as CommandHost)) {
    return host as CommandHostSelection;
  }
  throw new Error(
    `Unsupported host "${host}". Supported values: all, ${COMMAND_HOSTS.join(", ")}.`
  );
}

export function formatInstallCommandsResult(
  result: InstallCommandsResult
): string {
  return result.items
    .map(
      (item) => `${item.action} ${item.host}: ${item.path} (${item.invocation})`
    )
    .join("\n");
}
